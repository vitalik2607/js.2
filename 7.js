//7) Запросить у пользователя сумму покупки и вывести сумму к оплате со скидкой: от 200 до 300 – скидка будет
// 3%, от 300 до 500 – 5%, от 500 и выше – 7%.
let summ = +prompt("Введите сумму");

if(summ >= 200 && summ <= 300){
    alert(`${summ - (summ * 0.03)}`);
}else if (summ >= 300 && summ <= 500){
    alert(`${summ - (summ * 0.05)}`);
}else if (summ >= 500){
    alert(`${summ - (summ * 0.07)}`);
}else{
    alert("Error");
}